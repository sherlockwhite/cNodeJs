<template>
<div class="topic">
  <div class="left">
    <div class="title">
      <div class="title" v-html="topic.title"/>
      <div class='info'>
        <span>发布于&nbsp;{{$moment(topic.create_at,'YYYY-MM-DD').startOf('day').fromNow()}}&nbsp;•&nbsp;</span>
        作者：<router-link :to="{path:'/user/'+topic.loginname}"></router-link>&nbsp;•&nbsp;
        <span>{{topic.visit_count}}次浏览！！&nbsp;.&nbsp;</span>
        <span>{{$tab[topic.tab] && $tab[topic.tab].name}}</span>
      </div>
      <Divider/>
    </div>
  </div>
  <div class="right"></div>
</div>
</template>
<script>
import Divider from '@/details/Divider'
import {getDetail} from '@/utils/api'
export default {
  name: 'topic',
  components: {
    Divider
  },
  data () {
    return {
      topic: {}
    }
  },
  methods: {
    fetchData (id) {
      getDetail(id).then(res => {
        this.topic = {...res.data, ...res.author}
      })
    }
  },
  created () {
    this.fetchData(this.$route.params.id)
  },
  beforeRouteUpdate (to, from, next) {
    this.fetchData(to.params.id)
    next()
  }
}
</script>
